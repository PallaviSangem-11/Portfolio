// Typing Effect for Hero Title
function typeWriter(element, text, speed = 100) {
    let i = 0;
    element.innerHTML = '';
    
    function type() {
        if (i < text.length) {
            element.innerHTML += text.charAt(i);
            i++;
            setTimeout(type, speed);
        }
    }
    
    type();
}

// Initialize typing effect when page loads
window.addEventListener('load', () => {
    const titleRole = document.querySelector('#typing-role');
    if (titleRole) {
        const originalText = titleRole.textContent;
        typeWriter(titleRole, originalText, 120);
    }
});

